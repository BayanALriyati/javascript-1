let role=prompt ('*,-,+,/');
let num1= Number (prompt ('num1'));
let  num2= Number (prompt ('num2'));
if ( role== "+"){
    console.log (num1 + num2) ;
    document.write (num1 + num2) ;
}
else if ( role== "-"){
    console.log (num1 - num2) ;
    document.write (num1 - num2) ;
}
else if (role== "*"){
    console.log (num1 * num2) ;
    document.write (num1 * num2) ;
} 
else if ( role== "/"){
    console.log (num1 / num2) ;
    document.write (num1 / num2) ;
}